/* ── Theme toggle ── */

function readStoredTheme() {
  try {
    return localStorage.getItem(THEME_KEY);
  } catch {
    return "";
  }
}

function writeStoredTheme(theme) {
  try {
    localStorage.setItem(THEME_KEY, theme);
  } catch {
    // Theme still applies for current view.
  }
}

function applyTheme(theme) {
  if (theme === "light") {
    doc.setAttribute("data-theme", "light");
  } else {
    doc.removeAttribute("data-theme");
  }
}

function toggleTheme() {
  const current = doc.getAttribute("data-theme") === "light" ? "light" : "dark";
  const next = current === "light" ? "dark" : "light";
  applyTheme(next);
  writeStoredTheme(next);
}

function initThemeToggle() {
  const saved = readStoredTheme();
  applyTheme(saved || "dark");

  if (themeToggle) {
    themeToggle.addEventListener("click", toggleTheme);
  }
}

/* ── Custom Cursor ── */

function initCustomCursor() {
  const cursor = document.getElementById("custom-cursor");
  if (!cursor) return;

  // Disable custom cursor on touch devices
  if (window.matchMedia("(pointer: coarse)").matches) {
    cursor.style.display = "none";
    return;
  }

  let mouseX = -100;
  let mouseY = -100;
  let cursorX = -100;
  let cursorY = -100;
  let isVisible = false;

  window.addEventListener("mousemove", (e) => {
    mouseX = e.clientX;
    mouseY = e.clientY;
    if (!isVisible) {
      cursor.style.opacity = "1";
      isVisible = true;
    }
  });

  document.addEventListener("mouseleave", () => {
    cursor.style.opacity = "0";
    isVisible = false;
  });

  document.addEventListener("mouseenter", () => {
    cursor.style.opacity = "1";
    isVisible = true;
  });

  function animate() {
    const dx = mouseX - cursorX;
    const dy = mouseY - cursorY;
    cursorX += dx * 0.22;
    cursorY += dy * 0.22;
    cursor.style.left = `${cursorX}px`;
    cursor.style.top = `${cursorY}px`;
    requestAnimationFrame(animate);
  }
  animate();

  // Set up event delegation for hover state cursor shifts
  document.body.addEventListener("mouseover", (e) => {
    const target = e.target.closest("a, button, input, select, textarea, [role='button'], .language-button");
    if (target) {
      if ((target.tagName === "INPUT" && (target.type === "text" || target.type === "email" || target.type === "search")) || target.tagName === "TEXTAREA") {
        cursor.className = "custom-cursor cursor-text";
      } else {
        cursor.className = "custom-cursor cursor-pointer";
      }
    } else {
      cursor.className = "custom-cursor";
    }
  });
}

/* ── Scroll Reveal ── */

function initScrollReveal() {
  const revealElements = document.querySelectorAll(".reveal");
  if (revealElements.length === 0) return;

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      root: null,
      threshold: 0.08,
      rootMargin: "0px 0px -40px 0px",
    }
  );

  revealElements.forEach((el) => observer.observe(el));
}

/* ── Portfolios Loader ── */

function initPortfolios() {
  const grid = document.getElementById("portfolios-grid");
  if (!grid) return;

  fetch("assets/portfolios.json")
    .then((res) => res.json())
    .then((data) => {
      portfoliosData = data;
      renderPortfolios();
    })
    .catch((err) => console.error("Error loading portfolios:", err));
}

function renderPortfolios() {
  const grid = document.getElementById("portfolios-grid");
  if (!grid) return;

  const currentLang = readStoredLanguage() || "en";
  grid.innerHTML = "";

  if (!portfoliosData || !portfoliosData.length) return;

  portfoliosData.forEach((p, idx) => {
    const title = currentLang === "tr" ? p.title_tr : p.title_en;
    const desc = currentLang === "tr" ? p.desc_tr : p.desc_en;
    const cat = currentLang === "tr" ? p.category_tr : p.category_en;
    const bionlukText = currentLang === "tr" ? "Bionluk'ta Gör" : "View on Bionluk";
    
    const card = document.createElement("article");
    card.className = `portfolio-card reveal delay-${(idx + 1) * 100}`;
    
    card.innerHTML = `
      <a class="portfolio-link" href="${p.link}" target="_blank" rel="noopener noreferrer">
        <div class="portfolio-image-wrapper">
          <img class="portfolio-image" src="${p.image}" alt="${title}" loading="lazy">
          <span class="portfolio-badge">${cat}</span>
        </div>
        <div class="portfolio-info">
          <h3 class="portfolio-card-title">${title}</h3>
          <p class="portfolio-card-desc">${desc}</p>
          <span class="portfolio-action-link">${bionlukText} &rarr;</span>
        </div>
      </a>
    `;
    grid.appendChild(card);
  });

  // Observe newly loaded reveal elements
  const revealElements = grid.querySelectorAll(".reveal");
  if (window.IntersectionObserver) {
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("is-visible");
            observer.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.08, rootMargin: "0px 0px -40px 0px" }
    );
    revealElements.forEach((el) => observer.observe(el));
  }
}

