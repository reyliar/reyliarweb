const DEFAULT_LANGUAGE = "en";
const STORAGE_KEY = "portfolio-language";
const doc = document.documentElement;
const metaDescription = document.querySelector("meta[name='description']");
const header = document.querySelector(".site-header");
const languageButtons = [...document.querySelectorAll("[data-lang-button]")];
const navLinks = [...document.querySelectorAll("[data-nav-link]")];
const sections = navLinks
  .map((link) => document.querySelector(link.getAttribute("href")))
  .filter(Boolean);

function currentCopy(language) {
  return COPY[language] || COPY[DEFAULT_LANGUAGE];
}

function setLanguage(language) {
  const nextLanguage = COPY[language] ? language : DEFAULT_LANGUAGE;
  const copy = currentCopy(nextLanguage);

  doc.lang = nextLanguage;
  document.title = copy.title;
  if (metaDescription) metaDescription.setAttribute("content", copy.description);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (key && copy[key]) node.textContent = copy[key];
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    const key = node.dataset.i18nAriaLabel;
    if (key && copy[key]) node.setAttribute("aria-label", copy[key]);
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.langButton === nextLanguage;
    button.setAttribute("aria-pressed", String(isActive));
  });

  writeStoredLanguage(nextLanguage);
}

function readStoredLanguage() {
  try {
    return localStorage.getItem(STORAGE_KEY);
  } catch {
    return "";
  }
}

function writeStoredLanguage(language) {
  try {
    localStorage.setItem(STORAGE_KEY, language);
  } catch {
    // Language switching still works for the current page view.
  }
}

function initLanguageSwitch() {
  const savedLanguage = readStoredLanguage();
  setLanguage(savedLanguage || DEFAULT_LANGUAGE);

  languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
      setLanguage(button.dataset.langButton);
    });
  });
}

function updateHeaderState() {
  if (!header) return;
  header.classList.toggle("is-scrolled", window.scrollY > 18);
}

function initActiveNavigation() {
  if (!("IntersectionObserver" in window) || sections.length === 0) return;

  const observer = new IntersectionObserver((entries) => {
    const visible = entries
      .filter((entry) => entry.isIntersecting)
      .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];

    if (!visible) return;

    navLinks.forEach((link) => {
      const isActive = link.getAttribute("href") === `#${visible.target.id}`;
      link.classList.toggle("is-active", isActive);
      if (isActive) {
        link.setAttribute("aria-current", "true");
      } else {
        link.removeAttribute("aria-current");
      }
    });
  }, {
    rootMargin: "-30% 0px -55% 0px",
    threshold: [0.2, 0.45, 0.7],
  });

  sections.forEach((section) => observer.observe(section));
}

initLanguageSwitch();
initActiveNavigation();
updateHeaderState();
window.addEventListener("scroll", updateHeaderState, { passive: true });
